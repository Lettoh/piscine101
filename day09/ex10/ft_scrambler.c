void	ft_scrambler(int ***a, int *b, int *******c, int ****d)
{
	int swap;
	int swap2;
	int swap3;

	swap = ***a;
	***a = *b;
	swap2 = *******c;
	*******c = swap;
	swap3 = ****d;
	****d = swap2;
	*b = swap3;
}
